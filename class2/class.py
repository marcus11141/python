# 字串串接
a = "Hello"
b = "World"
c = a + " " + b  # 字串相加
print(c)


a = a * 3  # 字串乘法
print(a)


# 認識基本指令
# 指令會有名稱跟括號組成，()裡面放提供給指令的參數
# 每個參數都要用逗號隔開
m = max(1, 2, 3, 4, 5, 6)  # 取最大值
m2 = mini(4, 5, 6, 7, 8, 9, 10)  # 取最小值
print(m)

le = len("Hello")  # 取字串長度
print(le)

# type() 取得參數的型態
t1 = type(1)  # 取型態
print(t1)
t2 = type("Hello")  # 取型態
print(t2)
t3 = type(1.0)  # 取型態
print(t3)
t4 = type(True)  # 取型態
print(t4)
